import { Injectable } from '@angular/core';
import { HttpClient } from '@angular/common/http';
import { Observable } from 'rxjs';
import { ApiResponse } from 'app/entities/book/ApiResponse';
import { ApplicationConfigService } from 'app/core/config/application-config.service';
import { environment } from 'environments/environment';
import { SessionStorageService } from 'ngx-webstorage';

@Injectable({
  providedIn: 'root',
})
export class KomiteService {
  baseUrl: string = environment.baseUrl;
  untukSessionRole: any;
  constructor(
    protected http: HttpClient,
    protected applicationConfigService: ApplicationConfigService,
    private sessionStorageService: SessionStorageService
  ) {
    this.untukSessionRole = this.sessionStorageService.retrieve('sessionRole');
  }

  // //////////////////////////////////////////////// get Limit Approval non PTA ///////////////////////////////////////////////////////////////////////
  protected getLimitApprovalNonPTAUrl = this.applicationConfigService.getEndpointFor(
    this.baseUrl + 'v1/efos-approval/getLimitApprovalNonPTA'
  );
  // //////////////////////////////////////////////// get Limit Approval non PTA ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// get Limit Approval PTA ///////////////////////////////////////////////////////////////////////
  protected getLimitApprovalPTAUrl = this.applicationConfigService.getEndpointFor(this.baseUrl + 'v1/efos-approval/getLimitApprovalPTA');
  // //////////////////////////////////////////////// get Limit Approval PTA ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// getLimitDirBisnisNonPTAUrl ///////////////////////////////////////////////////////////////////////
  protected getLimitDirBisnisNonPTAUrl = this.applicationConfigService.getEndpointFor(
    this.baseUrl + 'v1/efos-approval/getLimitDirBisnisNonPTA'
  );
  // //////////////////////////////////////////////// getLimitDirBisnisNonPTAUrl ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// getLimitDirBisnisPTA ///////////////////////////////////////////////////////////////////////
  protected getLimitDirBisnisPTAUrl = this.applicationConfigService.getEndpointFor(this.baseUrl + 'v1/efos-approval/getLimitDirBisnisPTA');
  // //////////////////////////////////////////////// getLimitDirBisnisPTA ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// Get DirBisnis ///////////////////////////////////////////////////////////////////////
  protected getListAppDirbisnisUrl = this.applicationConfigService.getEndpointFor(this.baseUrl + 'v1/efos-approval/list_app_dirbisnis');
  // //////////////////////////////////////////////// Get DirBisnis ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// Get list_app_dirrisks ///////////////////////////////////////////////////////////////////////
  protected getListAppDirriskUrl = this.applicationConfigService.getEndpointFor(this.baseUrl + 'v1/efos-approval/list_app_dirrisk');
  // //////////////////////////////////////////////// Get list_app_dirrisk ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// Get list_app_dirut ///////////////////////////////////////////////////////////////////////
  protected getListAppDirutUrl = this.applicationConfigService.getEndpointFor(this.baseUrl + 'v1/efos-approval/list_app_dirut');
  // //////////////////////////////////////////////// Get list_app_dirut ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// Get list_app_komisaris ///////////////////////////////////////////////////////////////////////
  protected getListAppkomisarisUrl = this.applicationConfigService.getEndpointFor(this.baseUrl + 'v1/efos-approval/list_app_komisaris');
  // //////////////////////////////////////////////// Get list_app_komisaris ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// Get list_app_komut ///////////////////////////////////////////////////////////////////////
  protected getListAppkomisariutamasUrl = this.applicationConfigService.getEndpointFor(this.baseUrl + 'v1/efos-approval/list_app_komut');
  // //////////////////////////////////////////////// Get list_app_komut ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// Get Data Users ///////////////////////////////////////////////////////////////////////
  protected getDataUsersUrl = this.applicationConfigService.getEndpointFor(this.baseUrl + 'v1/efos-verif/getDataUsers?sd=');
  // //////////////////////////////////////////////// Get Data Users ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// Get Persetujuan Pembiayaan ///////////////////////////////////////////////////////////////////////
  protected getPersetujuanPembiayaanUrl = this.applicationConfigService.getEndpointFor(
    this.baseUrl + 'v1/efos-approval/list_history_persetujuan?si='
  );
  // //////////////////////////////////////////////// Get Persetujuan Pembiayaan ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// List Approval ///////////////////////////////////////////////////////////////////////
  protected getDetailApprovalByDE = this.applicationConfigService.getEndpointFor(this.baseUrl + 'v1/efos-approval/get_approval?si=');
  // //////////////////////////////////////////////// List Approval ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// Get Detail Approval ///////////////////////////////////////////////////////////////////////
  protected getListApproval = this.applicationConfigService.getEndpointFor(this.baseUrl + 'v1/efos-approval/list_app_approval');
  // //////////////////////////////////////////////// Get Detail Approval ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// List Persetujuan Khusus ///////////////////////////////////////////////////////////////////////
  protected ListPersetujuanKhusus = this.applicationConfigService.getEndpointFor(
    this.baseUrl + 'v1/efos-approval/list_persetujuan_khusus?si='
  );
  // //////////////////////////////////////////////// List Persetujuan Khusus ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// Ref Persetujuan Khusus ///////////////////////////////////////////////////////////////////////
  protected refPersetujuanKhusus = this.applicationConfigService.getEndpointFor(this.baseUrl + 'v1/efos-ref/list_persetujuan_khusus');
  // //////////////////////////////////////////////// Ref Persetujuan Khusus ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// List Approval ///////////////////////////////////////////////////////////////////////
  getDaftarAplikasiApproval(): Observable<ApiResponse> {
    return this.http.get<ApiResponse>(this.getListApproval);
  }
  // //////////////////////////////////////////////// List Approval ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// Get Detail Approval ///////////////////////////////////////////////////////////////////////
  getDetailApproval(app_no_de: string | undefined): Observable<ApiResponse> {
    return this.http.get<ApiResponse>(this.getDetailApprovalByDE + app_no_de);
  }
  // //////////////////////////////////////////////// Get Detail Approval ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// Ref Persetujuan Khusus ///////////////////////////////////////////////////////////////////////
  getRefPersetujuanKhusus(): Observable<ApiResponse> {
    return this.http.get<ApiResponse>(this.refPersetujuanKhusus);
  }
  // //////////////////////////////////////////////// Ref Persetujuan Khusus ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// List Persetujuan Khusus ///////////////////////////////////////////////////////////////////////
  getListPersetujuanKhusus(app_no_de: string | undefined): Observable<ApiResponse> {
    return this.http.get<ApiResponse>(this.ListPersetujuanKhusus + app_no_de);
  }
  // //////////////////////////////////////////////// List Persetujuan Khusus ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// Get Persetujuan Pembiayaan ///////////////////////////////////////////////////////////////////////
  getPersetujuanPembiayaan(app_no_de: string | undefined): Observable<ApiResponse> {
    return this.http.get<ApiResponse>(this.getPersetujuanPembiayaanUrl + app_no_de);
  }
  // //////////////////////////////////////////////// Get Persetujuan Pembiayaan ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// Get Data Users ///////////////////////////////////////////////////////////////////////
  getDataUsers(app_no_de: string | undefined): Observable<ApiResponse> {
    return this.http.get<ApiResponse>(this.getDataUsersUrl + app_no_de);
  }
  // //////////////////////////////////////////////// Get Data Users ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// Get DirBisnis ///////////////////////////////////////////////////////////////////////
  getListAppDirbisnis(): Observable<ApiResponse> {
    return this.http.get<ApiResponse>(this.getListAppDirbisnisUrl);
  }
  // //////////////////////////////////////////////// Get DirBisnis ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// Get DirBisnis all ///////////////////////////////////////////////////////////////////////
  getLimitAll(kode_fasilitas: any): Observable<ApiResponse> {
    if (this.untukSessionRole === 'DIRBISNIS') {
      if (kode_fasilitas === 'PTA') {
        // return this.http.get<ApiResponse>(this.getLimitDirBisnisPTAUrl);
        return this.http.get<ApiResponse>(this.getLimitDirBisnisNonPTAUrl);
      } else {
        return this.http.get<ApiResponse>(this.getLimitDirBisnisNonPTAUrl);
      }
      // return this.http.get<ApiResponse>(this.getLimitApprovalNonPTAUrl);
    } else {
      if (kode_fasilitas === 'PTA') {
        return this.http.get<ApiResponse>(this.getLimitApprovalPTAUrl);
      } else {
        return this.http.get<ApiResponse>(this.getLimitApprovalNonPTAUrl);
      }
      // return this.http.get<ApiResponse>(this.getLimitApprovalNonPTAUrl);
    }
    // return this.http.get<ApiResponse>(this.getLimitApprovalNonPTAUrl);
  }
  // //////////////////////////////////////////////// Get DirBisnis all ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// Get DirBisnis ///////////////////////////////////////////////////////////////////////
  getLimitApprovalNonPTA(): Observable<ApiResponse> {
    return this.http.get<ApiResponse>(this.getLimitApprovalNonPTAUrl);
  }
  // //////////////////////////////////////////////// Get DirBisnis ///////////////////////////////////////////////////////////////////////

  // //////////////////////////////////////////////// Get DirBisnis ///////////////////////////////////////////////////////////////////////
  getLimitApprovalPTA(): Observable<ApiResponse> {
    return this.http.get<ApiResponse>(this.getLimitApprovalPTAUrl);
  }
  // //////////////////////////////////////////////// Get DirBisnis ///////////////////////////////////////////////////////////////////////
}
